# Mojave Dark -------------------------------------------------------------

## URL
## https://github.com/patrickshox/Mojave-Dark-RStudio-Theme

## Codigo de descarga

## dinamico: los graficos se ven en negro cuando no posamos el mouse sobre ellos
rstudioapi::addTheme("https://raw.githubusercontent.com/patrickshox/Mojave-Dark-RStudio-Theme/master/Mojave%20Dark.rstheme", 
                     apply=TRUE, force=TRUE)

## Estatico: los graficos se ven como siempre
rstudioapi::addTheme("https://raw.githubusercontent.com/patrickshox/Mojave-Dark-RStudio-Theme/master/Mojave%20Dark%20(Static).rstheme", 
                     apply=TRUE, force=TRUE)





