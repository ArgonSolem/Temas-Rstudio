# Material Ocean ----------------------------------------------------------

## URL
## https://github.com/lusignan/RStudio-Material-Theme

## Codigo de descarga
rstudioapi::addTheme("https://raw.githubusercontent.com/lusignan/RStudio-Material-Theme/main/material-ocean.rstheme", 
                     apply = TRUE)





